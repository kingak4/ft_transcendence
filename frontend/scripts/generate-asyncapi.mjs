import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

import { TypeScriptGenerator } from '@asyncapi/modelina';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const BACKEND_URL = process.env.BACKEND_URL || 'http://localhost:8080';
const ASYNCAPI_URL = `${BACKEND_URL}/springwolf/docs`;

async function generate() {
  console.log(`Fetching AsyncAPI spec from ${ASYNCAPI_URL}...`);
  try {
    const response = await fetch(ASYNCAPI_URL);
    if (!response.ok) {
      throw new Error(`Failed to fetch AsyncAPI spec: ${response.statusText}`);
    }
    const spec = await response.json();

    // Sanitize spec to fix Springwolf quirks that cause strict parser validation errors
    if (spec.info && spec.info.extensionFields) {
      delete spec.info.extensionFields;
    }
    if (spec.operations) {
      for (const key in spec.operations) {
        // Remove security to avoid invalid $ref resolution errors (Modelina only needs payload types)
        delete spec.operations[key].security;
      }
    }

    fs.writeFileSync(path.join(__dirname, 'asyncapi-debug.json'), JSON.stringify(spec, null, 2));

    console.log('Generating TypeScript models...');
    const generator = new TypeScriptGenerator();

    // Modelina v3 only fully supports AsyncAPI v2. We can bypass the v3 parsing errors
    // by wrapping the schemas in a dummy AsyncAPI v2 document.
    // We also remove square brackets from Spring class names (like Message[B) because 
    // JSON Schema ref parser chokes on unescaped brackets in URIs.
    const replaceBrackets = (obj) => {
      if (typeof obj === 'string') {
        return obj.startsWith('#/') ? obj.replace(/\[/g, '_').replace(/\]/g, '_') : obj;
      }
      if (Array.isArray(obj)) {
        return obj.map(replaceBrackets);
      }
      if (obj !== null && typeof obj === 'object') {
        const newObj = {};
        for (const [key, value] of Object.entries(obj)) {
          const newKey = key.replace(/\[/g, '_').replace(/\]/g, '_');
          newObj[newKey] = replaceBrackets(value);
        }
        return newObj;
      }
      return obj;
    };

    const safeSchemas = replaceBrackets(spec.components.schemas || {});

    const dummySpec = {
      asyncapi: "2.6.0",
      info: { title: "Generated", version: "1.0.0" },
      channels: {},
      components: {
        schemas: safeSchemas
      }
    };

    // Force Modelina to see all schemas by creating dummy channels for each
    for (const schemaName in safeSchemas) {
      dummySpec.channels[`dummy_${schemaName}`] = {
        publish: {
          message: {
            payload: {
              $ref: `#/components/schemas/${schemaName}`
            }
          }
        }
      };
    }

    const models = await generator.generate(dummySpec);

    let output = `/**\n * This file was auto-generated by @asyncapi/modelina.\n * Do not make direct changes to the file.\n */\n/* eslint-disable @typescript-eslint/no-explicit-any */\n\n`;

    for (const model of models) {
      output += model.dependencies.join('\n') + '\n';
      output += model.result + '\n\n';
    }

    const outputPath = path.join(__dirname, '../app/types/asyncapi.d.ts');
    fs.mkdirSync(path.dirname(outputPath), { recursive: true });
    fs.writeFileSync(outputPath, output);

    console.log(`Successfully generated AsyncAPI models to ${outputPath}`);
  } catch (error) {
    console.error('Error generating AsyncAPI models:', error);
    process.exit(1);
  }
}

generate();
